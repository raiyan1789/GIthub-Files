function setup() {
  createCanvas (400, 400);//Canvas Size
                

                  }
                  
  


function draw() {//draws the shape and its attributes
  background(0, 255, 0)//background color
  stroke(255, 0, 0);//stroke color RGB
  strokeWeight (4);//Thickness of stroke
  fill(255, 0, 0)//color of the circle or ellipse in this case
  ellipse(230, 200, 150, 150)// x and y positions the first two numbers and width and height of ellipse
  
  
}